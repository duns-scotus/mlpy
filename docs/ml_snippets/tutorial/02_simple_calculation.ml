import console;

// Define some numbers
width = 10;
height = 5;

// Calculate area
area = width * height;

// Display result
console.log("Rectangle dimensions: " + str(width) + " x " + str(height));
console.log("Area: " + str(area));

// Calculate perimeter
perimeter = 2 * (width + height);
console.log("Perimeter: " + str(perimeter));

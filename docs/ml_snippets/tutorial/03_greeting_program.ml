// Demonstrates: String operations and console output
// ============================================

import console;

// Define user information
userName = "Alice";
userAge = 25;
userCity = "New York";

// Create greeting message
greeting = "Hello, " + userName + "!";
info = "You are " + str(userAge) + " years old and live in " + userCity + ".";

// Display greeting
console.log(greeting);
console.log(info);

// Calculate years until 30
yearsTo30 = 30 - userAge;
console.log("You will be 30 in " + str(yearsTo30) + " years.");
